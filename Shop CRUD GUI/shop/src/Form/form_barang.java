    public void TampilData() {
        DftTblModel_barang = new DefaultTableModel();
        DftTblModel_barang.addColumn("ID");
        DftTblModel_barang.addColumn("First Name");
        DftTblModel_barang.addColumn("Last Name");
        DftTblModel_barang.addColumn("Email");
        DftTblModel_barang.addColumn("Gender");
        DftTblModel_barang.addColumn("IP");
        jTable_barang.setModel(DftTblModel_barang);
        Connection conn = Koneksi.getConnection(selection);
        try {
            Statement stmt = conn.createStatement();
            SQL = "select * from mock_data";
            ResultSet res = stmt.executeQuery(SQL);
            while (res.next()) {
                DftTblModel_barang.addRow(new Object[]{
                        res.getString("id"),
                        res.getString("first_name"),
                        res.getString("last_name"),
                        res.getString("email"),
                        res.getString("gender"),
                        res.getString("ip_address")
                });
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }

