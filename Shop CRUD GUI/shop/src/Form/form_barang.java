    private void jButton_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_editActionPerformed
        try {
                    Connection conn = Koneksi.getConnection(selection);
                    PreparedStatement stmt = conn.prepareStatement("update mock_data set first_name=?, last_name=?, email=?, gender=?, ip_address=? where id=?");
                    stmt.setString(1, fnTextField.getText());
                    stmt.setString(2, lnTextField.getText());
                    stmt.setString(3, emailTextField.getText());
                    stmt.setString(4, genderTextField.getText());
                    stmt.setString(5, ipTextField.getText());
                    stmt.setString(6, idTextField.getText());
                    stmt.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Data berhasil diubah", "Pesan", JOptionPane.INFORMATION_MESSAGE);
                    TampilData();
                } catch (SQLException o) {
                    System.out.println(o.getMessage());
                }
    }//GEN-LAST:event_jButton_editActionPerformed

    private void jButton_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_hapusActionPerformed
        Connection conn = Koneksi.getConnection(selection);
                int confirm = JOptionPane.showConfirmDialog(null, "Apakah anda yakin ingin menghapus data tersebut?", "Konfirmasi", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
                if (confirm == 0) {
                    try {
                        PreparedStatement stmt = conn.prepareStatement("delete from mock_data where id ='" + idTextField.getText() + "'");
                        stmt.executeUpdate();
                        JOptionPane.showMessageDialog(null, "Data berhasil dihapus", "Pesan", JOptionPane.INFORMATION_MESSAGE);
                        TampilData();
                        idTextField.setText("");
                        fnTextField.setText("");
                        lnTextField.setText("");
                        emailTextField.setText("");
                        genderTextField.setText("");
                        ipTextField.setText("");
                        idTextField.requestFocus();
                    } catch (SQLException o) {
                        JOptionPane.showMessageDialog(null, "Data gagal di hapus" + o.getMessage(), "Pesan", JOptionPane.ERROR_MESSAGE);
                    }
                }
    }//GEN-LAST:event_jButton_hapusActionPerformed

