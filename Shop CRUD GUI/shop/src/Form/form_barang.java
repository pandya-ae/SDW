    private void jButton_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_simpanActionPerformed
        try {
                    Connection conn = Koneksi.getConnection(selection);
                    PreparedStatement stmt = conn.prepareStatement("insert into mock_data(id, first_name, last_name, email, gender, ip_address) values(?,?,?,?,?,?)");
                    stmt.setString(1, idTextField.getText());
                    stmt.setString(2, fnTextField.getText());
                    stmt.setString(3, lnTextField.getText());
                    stmt.setString(4, emailTextField.getText());
                    stmt.setString(5, genderTextField.getText());
                    stmt.setString(6, ipTextField.getText());
                    stmt.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Data berhasil disimpan", "Pesan", JOptionPane.INFORMATION_MESSAGE);
                    TampilData();
                } catch (SQLException o) {
                    System.out.println(o.getMessage());
                }
    }//GEN-LAST:event_jButton_simpanActionPerformed

